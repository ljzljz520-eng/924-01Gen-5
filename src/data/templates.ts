export const generateSlides = (
  industry: Industry,
  fundingStage: FundingStage,
  pageTypes: PageType[]
): PPTSlide[] => {
  const slides: PPTSlide[] = [];

  pageTypes.forEach((pageType, index) => {
    const template = templates[industry]?.[fundingStage]?.[pageType];
    if (template) {
      const slideId = `slide-${index + 1}`;
      const slide: PPTSlide = {
        id: slideId,
        type: pageType,
        title: template.title,
        subtitle: template.subtitle,
        content: template.content,
        bulletPoints: template.bulletPoints,
        charts: template.charts?.map((chart) => ({
          ...chart,
          id: generateId(),
          pageId: slideId,
        })),
        hasSampleData: template.hasSampleData,
        sampleDataNotes: template.sampleDataNotes,
        comments: [],
      };
      slides.push(slide);
    }
  });

  return slides;
};

export const getSampleDataSummary = (slides: PPTSlide[]) => {
  const sampleDataSlides = slides.filter((slide) => slide.hasSampleData);
  const summary = sampleDataSlides.map((slide, index) => ({
    index: index + 1,
    pageTitle: slide.title,
    notes: slide.sampleDataNotes,
  }));
  return {
    totalSampleDataSlides: sampleDataSlides.length,
    totalSlides: slides.length,
    details: summary,
  };
};
